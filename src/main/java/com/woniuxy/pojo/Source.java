package com.woniuxy.pojo;

public class Source {
    private Integer sourceId;

    private Integer queueId;

    private Integer skillgroupId;

    private Integer typeId;

    private Integer sourceNum;

    public Integer getSourceId() {
        return sourceId;
    }

    public void setSourceId(Integer sourceId) {
        this.sourceId = sourceId;
    }

    public Integer getQueueId() {
        return queueId;
    }

    public void setQueueId(Integer queueId) {
        this.queueId = queueId;
    }

    public Integer getSkillgroupId() {
        return skillgroupId;
    }

    public void setSkillgroupId(Integer skillgroupId) {
        this.skillgroupId = skillgroupId;
    }

    public Integer getTypeId() {
        return typeId;
    }

    public void setTypeId(Integer typeId) {
        this.typeId = typeId;
    }

    public Integer getSourceNum() {
        return sourceNum;
    }

    public void setSourceNum(Integer sourceNum) {
        this.sourceNum = sourceNum;
    }
}